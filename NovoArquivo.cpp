#include <iostream>

int main() {
    int inteiro = 10;
    float flutuante = 5.5f;
    double duplo = 10.99;
    char caractere = 'A';
    bool logico = true;

    std::cout << "Inteiro: " << inteiro << std::endl;
    std::cout << "Float: " << flutuante << std::endl;
    std::cout << "Double: " << duplo << std::endl;
    std::cout << "Caractere: " << caractere << std::endl;
    std::cout << "Logico: " << logico << std::endl;

    return 0;
}
